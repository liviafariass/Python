#Crie uma lista de frutas e imprima cada fruta usando um laço for.
frutas = ['maçã', 'pera', 'banana', 'melão', 'melancia', 'goiaba']

for fruta in frutas:
    print(fruta)

#Você criou uma lista chamada frutas. A lista tem 6 elementos: 'maçã', 'pera', 'banana', 'melão', 'melancia', 'goiaba'.
#Aqui você está criando um loop que vai passar por cada elemento da lista. fruta é uma variável que você criou ali no próprio for.Na primeira volta, fruta = 'maçã'. Na segunda volta, fruta = 'pera'. Na terceira volta, fruta = 'banana'. E assim por diante até o último elemento. pense que o loop está pegando cada fruta da lista, uma por vez, e colocando dentro da variável fruta.
#print(fruta): A cada volta do loop, ele imprime o valor atual de fruta.