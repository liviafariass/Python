#Escreva um programa que solicite uma frase ao usuário e exiba a frase em maiúsculas, minúsculas e com a primeira letra de cada palavra em maiúscula.
frase = str(input('Escreva uma frase:'))
print(frase.capitalize()) #a frase com a primeira letra maiuscula
print(frase.upper())#frase em maiuscula
print(frase.lower())#frase em minuscula